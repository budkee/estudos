let contador = 1
while (contador <= 10) {
    console.log(`contador = ${contador}`)
    contador++
}

for (let i = 1; i <= 10; i++) {
    console.log(`i = ${i}`)
}

const notas = [3, 4, 1.1, 10, 6.8]; 
for (let i = 0; i < notas.length; i++) {
    console.log(`nota = ${notas[i]}`)
} // Navegando um array